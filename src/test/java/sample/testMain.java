    private static void splitToFire(String arg) throws InterruptedException {
        for (int i = 0; i < arg.length(); i++) {
            if (i < arg.length() - 1) {
                if (setButtonsTest.contains(String.valueOf(arg.charAt(i) + String.valueOf(arg.charAt(i + 1))))) {
                    fireButton(String.valueOf(arg.charAt(i) + String.valueOf(arg.charAt(1 + i))));
                    i++;
                } else {
                    fireButton(String.valueOf(arg.charAt(i)));
                }
            } else {
                fireButton(String.valueOf(arg.charAt(i)));
            }
        }
    }
